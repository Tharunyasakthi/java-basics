package com.basics;

public class switchcase{
	public static void main(String args[]) {
		int a;
		a=1.;
		switch(a)
		{
			case 1 :
			   System.out.println(50);
			   break;
		    case 2:
		    	System.out.println(500);
		    	break;
            default:
            	System.out.println("false");
		}
	}

}
